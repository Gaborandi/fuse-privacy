const FUSE_RTL_LANGS = new Set(["ar"]);

function getInitialFuseLanguage() {
  const saved = window.localStorage.getItem("fuse-site-language");
  if (saved && FUSE_TRANSLATIONS[saved]) return saved;
  const browser = (navigator.language || "en").slice(0, 2).toLowerCase();
  return FUSE_TRANSLATIONS[browser] ? browser : "en";
}

function applyFuseLanguage(lang) {
  const dictionary = FUSE_TRANSLATIONS[lang] || FUSE_TRANSLATIONS.en;
  const fallback = FUSE_TRANSLATIONS.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = FUSE_RTL_LANGS.has(lang) ? "rtl" : "ltr";
  document.title = dictionary["meta.title"] || fallback["meta.title"];
  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute("content", dictionary["meta.description"] || fallback["meta.description"]);
  }
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    node.textContent = dictionary[key] || fallback[key] || node.textContent;
  });
  document.querySelectorAll("[data-language-select]").forEach((select) => {
    select.value = lang;
    select.setAttribute("aria-label", dictionary["nav.language"] || fallback["nav.language"]);
  });
  window.localStorage.setItem("fuse-site-language", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const initial = getInitialFuseLanguage();
  applyFuseLanguage(initial);
  document.querySelectorAll("[data-language-select]").forEach((select) => {
    select.addEventListener("change", (event) => {
      applyFuseLanguage(event.target.value);
    });
  });
});
